     private Color createColorFromRGBTextFields(){
        
        try{
            Integer r = Integer.valueOf(changeTitleColorRedTF.getText());
            Integer g = Integer.valueOf(changeTitleColorGreenTF.getText());
            Integer b = Integer.valueOf(changeTitleColorBlueTF.getText());
        
            if(r<=255 && r>=0 && g<=255 && g>=0 && b<=255 && b>=0){
                return new Color(r,g,b);
            }else throw new NumberFormatException();
            
        }catch(NumberFormatException ex){
            System.out.println("Nieprawidłowy kolor. R, G i B muszą wynosić 0-255.");
            return Color.BLACK;
        }
    }
     

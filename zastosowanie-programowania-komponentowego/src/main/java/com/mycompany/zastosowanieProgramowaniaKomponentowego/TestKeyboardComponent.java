    private Font getFontFromFontCB(){
        
        Font font;
       
        switch (fontCB.getSelectedIndex()) {
            case 0:
                font = new Font("Monospaced", Font.PLAIN, 24);
                break;
            case 1:
                font = new Font("Times New Roman", Font.PLAIN, 24);
                break;
            default:
                font = new Font("Baskerville Old Face", Font.PLAIN, 24);
                break;
        }
        
        return font;
        
    }
    
    
